extension TextSpanExtension on TextSpan {
  TextSpan font25W500({Color? color, TextDecoration? textDecoration}) {
    return TextSpan(
      text: text ?? '',
      style: TextStyle(
        fontSize: 25,
        color: color ?? ThemeHelper.captionColor,
        decoration: textDecoration,
        fontWeight: FontWeight.w500,
      ),
    );
  }

  TextSpan font24W400({Color? color, TextDecoration? textDecoration}) {
    return TextSpan(
      text: text ?? '',
      style: TextStyle(
        fontSize: 24,
        color: color ?? ThemeHelper.captionColor,
        decoration: textDecoration,
        fontWeight: FontWeight.w400,
      ),
    );
  }
}
